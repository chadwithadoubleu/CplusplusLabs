#include <iostream>

using namespace std;

int main()
{
	cout << "2 + 3 = " << 2 + 3 << endl;
	cout << "17 % 4 = " << 17 % 4 << endl;
	cout << "4 + 9 = " << 4 + 9 << endl;
	cout << "46 / 7 = " << 46 / 7 << endl;
	cout << "46 % 7 = " << 46 / 7 << endl;
	cout << "2 * 3.0 = " << 2 * 3.0 << endl;
	cout << "float(25)/4 = " << float(25) / 4 << endl;
	cout << "int(7.75) + 2 = " << int(7.75) + 2 << endl;
	cout << "int('P') = " << int('P') << endl;
	cout << "char(105) = " << char(105) << endl;
}